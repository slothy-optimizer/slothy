.macro ldr_vo vec, base, offset                    // slothy:no-unfold
       ldr qform_\vec, [\base, \offset]
.endm

.macro ldr_vi vec, base, inc                        // slothy:no-unfold
        ldr qform_\vec, [\base], \inc
.endm

.macro str_vo vec, base, offset                     // slothy:no-unfold
