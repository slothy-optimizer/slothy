.data
roots:
#include "ntt_dilithium_twiddles.s"
.text

// Barrett multiplication
.macro mulmod dst, src, const, const_twisted
        vmul.s32       \dst,  \src, \const
        vqrdmulh.s32   \src,  \src, \const_twisted
        vmla.s32       \dst,  \src, modulus
.endm

.macro ct_butterfly a, b, root, root_twisted
        mulmod tmp, \b, \root, \root_twisted
        vsub.u32       \b,    \a, tmp
        vadd.u32       \a,    \a, tmp
.endm

.align 4
roots_addr: .word roots
.syntax unified
.type ntt_dilithium, %function
.global ntt_dilithium
ntt_dilithium:

        push {r4-r11,lr}
        // Save MVE vector registers
        vpush {d8-d15}

        modulus  .req r12
        root_ptr .req r11

        .equ modulus_const, -8380417
        movw modulus, #:lower16:modulus_const
        movt modulus, #:upper16:modulus_const
        ldr  root_ptr, roots_addr

        in_low       .req r0
        in_high      .req r1

        add in_high, in_low, #(4*128)

        root0         .req r2
        root0_twisted .req r3
        root1         .req r4
        root1_twisted .req r5
        root2         .req r6
        root2_twisted .req r7

        data0 .req q0
        data1 .req q1
        data2 .req q2
        data3 .req q3

        tmp .req q4

        // Layers 1-2

        ldrd root0, root0_twisted, [root_ptr], #+8
        ldrd root1, root1_twisted, [root_ptr], #+8
        ldrd root2, root2_twisted, [root_ptr], #+8

        mov lr, #16
        vldrw.u32 q0, [in_high, #256]              // .*....
        vqrdmulh.s32 q5, q0, root0_twisted         // ...*..
        vldrw.u32 q3, [in_low, #256]               // *.....
        vmul.s32 q0, q0, root0                     // ..*...
        nop                                        // .....*
        vmla.s32 q0, q5, modulus                   // ....*.
        
        // original source code
        // vldrw.u32 q3, [in_low, #256]            // ..*...
        // vldrw.u32 q7, [in_high, #256]           // *.....
        // vmul.s32 q0, q7, root0                  // ...*..
        // vqrdmulh.s32 q1, q7, root0_twisted      // .*....
        // vmla.s32 q0, q1, modulus                // .....*
        // nop                                     // ....*.
        
        sub lr, lr, #1
        wls lr, lr, layer12_loop_end
layer12_loop:
        vsub.u32 q4, q3, q0                        // ............*...............
        vmul.s32 q6, q4, root2                     // ...................*........
        vldrw.u32 q1, [in_high]                    // ..*.........................
        vmul.s32 q5, q1, root0                     // ....*.......................
        vadd.u32 q7, q3, q0                        // .............*..............
        vqrdmulh.s32 q3, q1, root0_twisted         // .....*......................
        vldrw.u32 q0, [in_low]                     // *...........................
        vmla.s32 q5, q3, modulus                   // ......*.....................
        vldrw.u32 q3, [in_low, #272]               // .e..........................
        vmul.s32 q1, q7, root1                     // ..............*.............
        vsub.u32 q2, q0, q5                        // .......*....................
        vqrdmulh.s32 q7, q7, root1_twisted         // ...............*............
        vadd.u32 q5, q0, q5                        // ........*...................
        vmla.s32 q1, q7, modulus                   // ................*...........
        vldrw.u32 q7, [in_high, #272]              // ...e........................
        vqrdmulh.s32 q0, q4, root2_twisted         // ....................*.......
        vadd.u32 q4, q5, q1                        // ..................*.........
        vmla.s32 q6, q0, modulus                   // .....................*......
        vstrw.u32 q4, [in_low] , #16               // ........................*...
        vadd.u32 q4, q2, q6                        // .......................*....
        vstrw.u32 q4, [in_high] , #16              // ..........................*.
        vmul.s32 q0, q7, root0                     // .........e..................
        vsub.u32 q5, q5, q1                        // .................*..........
        vqrdmulh.s32 q1, q7, root0_twisted         // ..........e.................
        vstrw.u32 q5, [in_low, #240]               // .........................*..
        vsub.u32 q7, q2, q6                        // ......................*.....
        vmla.s32 q0, q1, modulus                   // ...........e................
        vstrw.u32 q7, [in_high, #240]              // ...........................*
        
        // original source code
        // vldrw.u32 data0, [in_low]                     // ..........................*.....................
        // vldrw.u32 data1, [in_low, #256]               // e...............................................
        // vldrw.u32 data2, [in_high]                    // ......................*.........................
        // vldrw.u32 data3, [in_high, #256]              // ......e.........................................
        // vmul.s32 tmp, data2, root0                    // .......................*........................
        // vqrdmulh.s32 data2, data2, root0_twisted      // .........................*......................
        // vmla.s32 tmp, data2, modulus                  // ...........................*....................
        // vsub.u32 data2, data0, tmp                    // ..............................*.................
        // vadd.u32 data0, data0, tmp                    // ................................*...............
        // vmul.s32 tmp, data3, root0                    // .............e..................................
        // vqrdmulh.s32 data3, data3, root0_twisted      // ...............e................................
        // vmla.s32 tmp, data3, modulus                  // ..................e.............................
        // vsub.u32 data3, data1, tmp                    // ....................*...........................
        // vadd.u32 data1, data1, tmp                    // ........................*.......................
        // vmul.s32 tmp, data1, root1                    // .............................*..................
        // vqrdmulh.s32 data1, data1, root1_twisted      // ...............................*................
        // vmla.s32 tmp, data1, modulus                  // .................................*..............
        // vsub.u32 data1, data0, tmp                    // ..........................................*.....
        // vadd.u32 data0, data0, tmp                    // ....................................*...........
        // vmul.s32 tmp, data3, root2                    // .....................*..........................
        // vqrdmulh.s32 data3, data3, root2_twisted      // ...................................*............
        // vmla.s32 tmp, data3, modulus                  // .....................................*..........
        // vsub.u32 data3, data2, tmp                    // .............................................*..
        // vadd.u32 data2, data2, tmp                    // .......................................*........
        // vstrw.u32 data0, [in_low] , #16               // ......................................*.........
        // vstrw.u32 data1, [in_low, #240]               // ............................................*...
        // vstrw.u32 data2, [in_high] , #16              // ........................................*.......
        // vstrw.u32 data3, [in_high, #240]              // ...............................................*
        
        le lr, layer12_loop
layer12_loop_end:
        vldrw.u32 q4, [in_high]                    // ..*.....................
        vmul.s32 q5, q4, root0                     // ...*....................
        vadd.u32 q1, q3, q0                        // ....*...................
        vmul.s32 q6, q1, root1                     // ........*...............
        vsub.u32 q7, q3, q0                        // *.......................
        vqrdmulh.s32 q3, q4, root0_twisted         // .....*..................
        vldrw.u32 q0, [in_low]                     // ......*.................
        vmla.s32 q5, q3, modulus                   // .......*................
        nop                                        // .......................*
        vqrdmulh.s32 q1, q1, root1_twisted         // ..........*.............
        vsub.u32 q2, q0, q5                        // .........*..............
        vmla.s32 q6, q1, modulus                   // ............*...........
        vadd.u32 q4, q0, q5                        // ...........*............
        vmul.s32 q5, q7, root2                     // .*......................
        vadd.u32 q1, q4, q6                        // ..............*.........
        vstrw.u32 q1, [in_low] , #16               // ................*.......
        vqrdmulh.s32 q0, q7, root2_twisted         // .............*..........
        vsub.u32 q1, q4, q6                        // ...................*....
        vmla.s32 q5, q0, modulus                   // ...............*........
        vstrw.u32 q1, [in_low, #240]               // ....................*...
        vsub.u32 q1, q2, q5                        // .....................*..
        vstrw.u32 q1, [in_high, #256]              // ......................*.
        vadd.u32 q1, q2, q5                        // .................*......
        vstrw.u32 q1, [in_high] , #16              // ..................*.....
        
        // original source code
        // vsub.u32 q4, q3, q0                     // ....*...................
        // vmul.s32 q6, q4, root2                  // .............*..........
        // vldrw.u32 q1, [in_high]                 // *.......................
        // vmul.s32 q5, q1, root0                  // .*......................
        // vadd.u32 q7, q3, q0                     // ..*.....................
        // vqrdmulh.s32 q3, q1, root0_twisted      // .....*..................
        // vldrw.u32 q0, [in_low]                  // ......*.................
        // vmla.s32 q5, q3, modulus                // .......*................
        // vmul.s32 q1, q7, root1                  // ...*....................
        // vsub.u32 q2, q0, q5                     // ..........*.............
        // vqrdmulh.s32 q7, q7, root1_twisted      // .........*..............
        // vadd.u32 q5, q0, q5                     // ............*...........
        // vmla.s32 q1, q7, modulus                // ...........*............
        // vqrdmulh.s32 q0, q4, root2_twisted      // ................*.......
        // vadd.u32 q4, q5, q1                     // ..............*.........
        // vmla.s32 q6, q0, modulus                // ..................*.....
        // vstrw.u32 q4, [in_low] , #16            // ...............*........
        // vadd.u32 q4, q2, q6                     // ......................*.
        // vstrw.u32 q4, [in_high] , #16           // .......................*
        // vsub.u32 q5, q5, q1                     // .................*......
        // vstrw.u32 q5, [in_low, #240]            // ...................*....
        // vsub.u32 q7, q2, q6                     // ....................*...
        // vstrw.u32 q7, [in_high, #240]           // .....................*..
        // nop                                     // ........*...............
        

        .unreq in_high
        .unreq in_low

        in .req r0
        sub in, in, #(64*4)

        // Layers 3,4

        // 4 butterfly blocks per root config, 4 root configs
        // loop over root configs

        count .req r1
        mov count, #4

out_start:
        ldrd root0, root0_twisted, [root_ptr], #+8
        ldrd root1, root1_twisted, [root_ptr], #+8
        ldrd root2, root2_twisted, [root_ptr], #+8

        mov lr, #4
        vldrw.u32 q5, [in, #192]                   // .*....
        vmul.s32 q0, q5, root0                     // ..*...
        vldrw.u32 q3, [in, #64]                    // *.....
        vqrdmulh.s32 q1, q5, root0_twisted         // ...*..
        nop                                        // .....*
        vmla.s32 q0, q1, modulus                   // ....*.
        
        // original source code
        // vldrw.u32 q3, [in, #64]                 // ..*...
        // vldrw.u32 q7, [in, #192]                // *.....
        // vmul.s32 q0, q7, root0                  // .*....
        // vqrdmulh.s32 q1, q7, root0_twisted      // ...*..
        // vmla.s32 q0, q1, modulus                // .....*
        // nop                                     // ....*.
        
        sub lr, lr, #1
        wls lr, lr, layer34_loop_end
layer34_loop:
        vsub.u32 q4, q3, q0                        // ............*...............
        vmul.s32 q6, q4, root2                     // ...................*........
        vldrw.u32 q1, [in, #128]                   // ..*.........................
        vmul.s32 q5, q1, root0                     // ....*.......................
        vadd.u32 q7, q3, q0                        // .............*..............
        vqrdmulh.s32 q3, q1, root0_twisted         // .....*......................
        vldrw.u32 q0, [in]                         // *...........................
        vmla.s32 q5, q3, modulus                   // ......*.....................
        vldrw.u32 q3, [in, #80]                    // .e..........................
        vmul.s32 q1, q7, root1                     // ..............*.............
        vsub.u32 q2, q0, q5                        // .......*....................
        vqrdmulh.s32 q7, q7, root1_twisted         // ...............*............
        vadd.u32 q5, q0, q5                        // ........*...................
        vmla.s32 q1, q7, modulus                   // ................*...........
        vldrw.u32 q7, [in, #208]                   // ...e........................
        vqrdmulh.s32 q0, q4, root2_twisted         // ....................*.......
        vadd.u32 q4, q5, q1                        // ..................*.........
        vmla.s32 q6, q0, modulus                   // .....................*......
        vstrw.u32 q4, [in] , #16                   // ........................*...
        vadd.u32 q4, q2, q6                        // .......................*....
        vstrw.u32 q4, [in, #112]                   // ..........................*.
        vmul.s32 q0, q7, root0                     // .........e..................
        vsub.u32 q5, q5, q1                        // .................*..........
        vqrdmulh.s32 q1, q7, root0_twisted         // ..........e.................
        vstrw.u32 q5, [in, #48]                    // .........................*..
        vsub.u32 q7, q2, q6                        // ......................*.....
        vmla.s32 q0, q1, modulus                   // ...........e................
        vstrw.u32 q7, [in, #176]                   // ...........................*
        
        // original source code
        // vldrw.u32 data0, [in]                         // ..........................*.....................
        // vldrw.u32 data1, [in, #64]                    // e...............................................
        // vldrw.u32 data2, [in, #128]                   // ......................*.........................
        // vldrw.u32 data3, [in, #192]                   // ......e.........................................
        // vmul.s32 tmp, data2, root0                    // .......................*........................
        // vqrdmulh.s32 data2, data2, root0_twisted      // .........................*......................
        // vmla.s32 tmp, data2, modulus                  // ...........................*....................
        // vsub.u32 data2, data0, tmp                    // ..............................*.................
        // vadd.u32 data0, data0, tmp                    // ................................*...............
        // vmul.s32 tmp, data3, root0                    // .............e..................................
        // vqrdmulh.s32 data3, data3, root0_twisted      // ...............e................................
        // vmla.s32 tmp, data3, modulus                  // ..................e.............................
        // vsub.u32 data3, data1, tmp                    // ....................*...........................
        // vadd.u32 data1, data1, tmp                    // ........................*.......................
        // vmul.s32 tmp, data1, root1                    // .............................*..................
        // vqrdmulh.s32 data1, data1, root1_twisted      // ...............................*................
        // vmla.s32 tmp, data1, modulus                  // .................................*..............
        // vsub.u32 data1, data0, tmp                    // ..........................................*.....
        // vadd.u32 data0, data0, tmp                    // ....................................*...........
        // vmul.s32 tmp, data3, root2                    // .....................*..........................
        // vqrdmulh.s32 data3, data3, root2_twisted      // ...................................*............
        // vmla.s32 tmp, data3, modulus                  // .....................................*..........
        // vsub.u32 data3, data2, tmp                    // .............................................*..
        // vadd.u32 data2, data2, tmp                    // .......................................*........
        // vstrw.u32 data0, [in] , #16                   // ......................................*.........
        // vstrw.u32 data1, [in, #48]                    // ............................................*...
        // vstrw.u32 data2, [in, #112]                   // ........................................*.......
        // vstrw.u32 data3, [in, #176]                   // ...............................................*
        
        le lr, layer34_loop
layer34_loop_end:
        vldrw.u32 q4, [in, #128]                   // ..*.....................
        vmul.s32 q5, q4, root0                     // ...*....................
        vadd.u32 q1, q3, q0                        // ....*...................
        vmul.s32 q6, q1, root1                     // ........*...............
        vsub.u32 q7, q3, q0                        // *.......................
        vqrdmulh.s32 q3, q4, root0_twisted         // .....*..................
        vldrw.u32 q0, [in]                         // ......*.................
        vmla.s32 q5, q3, modulus                   // .......*................
        nop                                        // .......................*
        vqrdmulh.s32 q1, q1, root1_twisted         // ..........*.............
        vsub.u32 q2, q0, q5                        // .........*..............
        vmla.s32 q6, q1, modulus                   // ............*...........
        vadd.u32 q4, q0, q5                        // ...........*............
        vmul.s32 q5, q7, root2                     // .*......................
        vadd.u32 q1, q4, q6                        // ..............*.........
        vstrw.u32 q1, [in] , #16                   // ................*.......
        vqrdmulh.s32 q0, q7, root2_twisted         // .............*..........
        vsub.u32 q1, q4, q6                        // ...................*....
        vmla.s32 q5, q0, modulus                   // ...............*........
        vstrw.u32 q1, [in, #48]                    // ....................*...
        vsub.u32 q1, q2, q5                        // .....................*..
        vstrw.u32 q1, [in, #176]                   // ......................*.
        vadd.u32 q1, q2, q5                        // .................*......
        vstrw.u32 q1, [in, #112]                   // ..................*.....
        
        // original source code
        // vsub.u32 q4, q3, q0                     // ....*...................
        // vmul.s32 q6, q4, root2                  // .............*..........
        // vldrw.u32 q1, [in, #128]                // *.......................
        // vmul.s32 q5, q1, root0                  // .*......................
        // vadd.u32 q7, q3, q0                     // ..*.....................
        // vqrdmulh.s32 q3, q1, root0_twisted      // .....*..................
        // vldrw.u32 q0, [in]                      // ......*.................
        // vmla.s32 q5, q3, modulus                // .......*................
        // vmul.s32 q1, q7, root1                  // ...*....................
        // vsub.u32 q2, q0, q5                     // ..........*.............
        // vqrdmulh.s32 q7, q7, root1_twisted      // .........*..............
        // vadd.u32 q5, q0, q5                     // ............*...........
        // vmla.s32 q1, q7, modulus                // ...........*............
        // vqrdmulh.s32 q0, q4, root2_twisted      // ................*.......
        // vadd.u32 q4, q5, q1                     // ..............*.........
        // vmla.s32 q6, q0, modulus                // ..................*.....
        // vstrw.u32 q4, [in] , #16                // ...............*........
        // vadd.u32 q4, q2, q6                     // ......................*.
        // vstrw.u32 q4, [in, #112]                // .......................*
        // vsub.u32 q5, q5, q1                     // .................*......
        // vstrw.u32 q5, [in, #48]                 // ...................*....
        // vsub.u32 q7, q2, q6                     // ....................*...
        // vstrw.u32 q7, [in, #176]                // .....................*..
        // nop                                     // ........*...............
        


        add in, in, #(4*64 - 4*16)
        subs count, count, #1
        bne out_start

        sub in, in, #(4*256)

        // Layers 5,6

        // 1 butterfly blocks per root config, 16 root configs
        // loop over root configs

        mov lr, #16
        ldrd r4, r6, [root_ptr] , #24          // ..*....
        vldrw.u32 q0, [in, #48]                // *......
        vqrdmulh.s32 q5, q0, r6                // ....*..
        ldrd r2, r8, [root_ptr, #-16]          // ...*...
        vmul.s32 q0, q0, r4                    // .....*.
        vldrw.u32 q2, [in, #16]                // .*.....
        vmla.s32 q0, q5, modulus               // ......*
        
        // original source code
        // vldrw.u32 q0, [in, #48]             // .*.....
        // vldrw.u32 q2, [in, #16]             // .....*.
        // ldrd r4, r6, [root_ptr] , #24       // *......
        // ldrd r2, r8, [root_ptr, #-16]       // ...*...
        // vqrdmulh.s32 q1, q0, r6             // ..*....
        // vmul.s32 q0, q0, r4                 // ....*..
        // vmla.s32 q0, q1, modulus            // ......*
        
        sub lr, lr, #1
        wls lr, lr, layer56_loop_end
layer56_loop:
        vadd.u32 q3, q2, q0                    // ................*..............
        vmul.s32 q1, q3, r2                    // .................*.............
        vldrw.u32 q4, [in, #32]                // .....*.........................
        vmul.s32 q7, q4, r4                    // .......*.......................
        ldrd r2, r1, [root_ptr, #-8]           // ..*............................
        vqrdmulh.s32 q6, q4, r6                // ........*......................
        vsub.u32 q0, q2, q0                    // ...............*...............
        vmla.s32 q7, q6, modulus               // .........*.....................
        vldrw.u32 q5, [in]                     // ...*...........................
        vqrdmulh.s32 q4, q3, r8                // ..................*............
        vadd.u32 q2, q5, q7                    // ...........*...................
        vmla.s32 q1, q4, modulus               // ...................*...........
        vsub.u32 q7, q5, q7                    // ..........*....................
        vmul.s32 q5, q0, r2                    // ......................*........
        vadd.u32 q3, q2, q1                    // .....................*.........
        vqrdmulh.s32 q0, q0, r1                // .......................*.......
        vsub.u32 q4, q2, q1                    // ....................*..........
        vmla.s32 q5, q0, modulus               // ........................*......
        vldrw.u32 q0, [in, #112]               // ......e........................
        vsub.u32 q6, q7, q5                    // .........................*.....
        vldrw.u32 q2, [in, #80]                // ....e..........................
        vadd.u32 q5, q7, q5                    // ..........................*....
        ldrd r4, r6, [root_ptr] , #24          // e..............................
        ldrd r2, r8, [root_ptr, #-16]          // .e.............................
        vst40.u32 {q3,q4,q5,q6}, [in]          // ...........................*...
        vqrdmulh.s32 q1, q0, r6                // .............e.................
